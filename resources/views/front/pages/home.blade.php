        function count(){
            $('.count').each(function () {
                $(this).prop('Counter', 0).animate({
                    Counter: $(this).data('value')
                }, {
                    duration: 5000,
                    easing: 'swing',
                    step: function (now) {
                        $(this).text(this.Counter.toFixed(0));
                    }
                });
            });
            this.check = false;
        }
        $(window).scroll(function (event) {

            if($(window).scrollTop()>0 && check){
                count();
            }
        })
